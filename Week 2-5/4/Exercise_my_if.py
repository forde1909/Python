#################################
# This is a OR Gate             #
# True = 1                      #
# False = 0                     #
# Any input True output True    #
# All inputs False output False #
#################################

# Inputs #
a = True
b = False
c = True

if a:
	print("a was true")
elif b:
	print("b was true")
elif c:
	print("c was true")
else:
	print("None of our boolean variables were true")
	
